package com.fallstudie.simulation.shared;

public class Produkt {
	
	private int menge;
	private double preis;


	// Getter-Setter-Methode
	public int getMenge() {
		return menge;
	}
	public void setMenge(int menge) {
		this.menge = menge;
	}
	public double getPreis() {
		return preis;
	}
	public void setPreis(double preis) {
		this.preis = preis;
	}
	
	

}
